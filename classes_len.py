# Criando uma lista com diferentes tipos de elementos
animais = ["cachorro", "gato", "pássaro", 5, 3.14, ["peixe", "rato"]]

# Adicionando um novo elemento à lista
animais.append("coelho")
animais.append("macaco")

# Exibindo a lista
print("Lista de animais:", animais)

# Obtendo e exibindo o tamanho da lista
tamanho = len(animais)
print("O tamanho da lista é:", tamanho)
